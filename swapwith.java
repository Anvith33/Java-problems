public class swapwith {
    public static void main(String[] args)
    {
        int temp;
        int a=1;
        int b=2;
        System.out.println("Before swapping:"+a+" "+b);
        temp=a;
        a=b;
        b=temp;
        System.out.println("After swapping:"+a+" "+b);
    }
   
    
}
